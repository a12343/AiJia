package com.aijia.table.contant;

/**
 * 自定义字段类型
 *
 * @author : yangjunqing / yangjunqing@seerbigdata.com
 * @version : 1.0
 */
public enum ColumnType {

    /**
     * 文本.
     */
    text,

    /**
     * 数值.
     */
    number,

    /**
     * 单选.
     */
    radio,

    /**
     * 多选.
     */
    checkbox,

    /**
     * 日期(yyyy-MM-dd).
     */
    date,

    /**
     * 时间(yyyy-MM-dd HH:mm:ss).
     */
    datetime,
    /**
     * 下拉列表.
     */
    select
}
